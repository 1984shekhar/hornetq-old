   public void testGetAll() throws Exception
   {
      list.addLast(a, 0);
      list.addLast(b, 3);
      list.addLast(c, 3);
      list.addLast(d, 3);
      list.addLast(e, 6);
      list.addLast(f, 6);
      list.addLast(g, 6);
      list.addLast(h, 9);
      list.addLast(i, 9);
      list.addLast(j, 9);
      
      
      Iterator<Wibble> iter = list.getAll().iterator();
      int count = 0;
      while (iter.hasNext())
      {
         Object o = iter.next();
         if (count == 0)
         {
            assertEquals(h, o);
         }
         if (count == 1)
         {
            assertEquals(i, o);
         }
         if (count == 2)
         {
            assertEquals(j, o);
         }
         if (count == 3)
         {
            assertEquals(e, o);
         }
         if (count == 4)
         {
            assertEquals(f, o);
         }
         if (count == 5)
         {
            assertEquals(g, o);
         }
         if (count == 6)
         {
            assertEquals(b, o);
         }
         if (count == 7)
         {
            assertEquals(c, o);
         }
         if (count == 8)
         {
            assertEquals(d, o);
         }
         if (count == 9)
         {
            assertEquals(a, o);
         }
         count++;
      }
      assertEquals(10, count);
   }
   
